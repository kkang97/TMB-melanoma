library("clusterProfiler")
library("org.Hs.eg.db")
library("enrichplot")
library("ggplot2")

pvalueFilter=0.05                #p值过滤条件
qvalueFilter=0.05                #矫正后的p值过滤条件

setwd("C:\\Users\\86181\\Desktop\\KK\\449vTMBimmune\\16.KEGG")              #设置工作目录
rt=read.table("id.txt",sep="\t",header=T,check.names=F)            #读取id.txt文件
rt=rt[is.na(rt[,"entrezID"])==F,]                                  #去除基因id为NA的基因
gene=rt$entrezID
geneFC=rt$logFC
names(geneFC)=gene

colorSel="qvalue"
if(qvalueFilter>0.05){
	colorSel="pvalue"
}

#kegg富集分析
kk <- enrichKEGG(gene = gene, organism = "hsa", pvalueCutoff =1, qvalueCutoff =1)   #富集分析
KEGG=as.data.frame(kk)
KEGG$geneID=as.character(sapply(KEGG$geneID,function(x)paste(rt$gene[match(strsplit(x,"/")[[1]],as.character(rt$entrezID))],collapse="/")))
KEGG=KEGG[(KEGG$pvalue<pvalueFilter & KEGG$qvalue<qvalueFilter),]
write.table(KEGG,file="KEGG.txt",sep="\t",quote=F,row.names = F)                          #保存富集结果

showNum=30
if(nrow(KEGG)<30){
	showNum=nrow(KEGG)
}

#柱状图
pdf(file="barplot.pdf",width = 10,height = 7)
barplot(kk, drop = TRUE, showCategory = showNum, color = colorSel)
dev.off()

#气泡图
pdf(file="bubble.pdf",width = 10,height = 7)
dotplot(kk, showCategory = showNum, orderBy = "GeneRatio",color = colorSel)
dev.off()
